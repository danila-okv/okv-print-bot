def format_print_text(data: dict) -> str:
    header = f"""
    ‚úÖ –§–∞–π–ª <b>{data["file_name"]}</b> –æ–±—Ä–∞–±–æ—Ç–∞–Ω

    üìÑ –°—Ç—Ä–∞–Ω–∏—Ü: <b>{data["page_count"]}</b>
    üí∞ –ö –æ–ø–ª–∞—Ç–µ: <b>{data["price"]:.2f} —Ä—É–±.</b>
    """
    
    opts = []
    
    pr = data.get("pages")
    if pr:
        opts.append(f"–°—Ç—Ä–∞–Ω–∏—Ü—ã: {pr}")
    
    duplex = data.get("duplex", False)
    if duplex:
        opts.append(f"–î–≤—É—Å—Ç–æ—Ä–æ–Ω–Ω—è—è –ø–µ—á–∞—Ç—å")
    
    layout = data.get("layout")
    if layout and layout != "1":
        opts.append(f"–ú–∞–∫–µ—Ç: {layout} –Ω–∞ –ª–∏—Å—Ç")

    if not opts:
        return header

    options_block = "\n".join(["", "‚ÄºÔ∏è <b>–í—ã–±—Ä–∞–Ω–Ω—ã–µ –æ–ø—Ü–∏–∏:</b>"] + opts)
    return header + options_block
